/////////////////////////////////////////////////////////////////////////////
// This hook is called after startup to initialize the application
/////////////////////////////////////////////////////////////////////////////
void APP_Init(void)
{
  // initialize all LEDs
  MIOS32_BOARD_LED_Init(0xffffffff);
  MIOS32_BOARD_LED_Set(0xffffffff, 0);

  // increase default verbose level (for this demo app...)
  MBNET_VerboseLevelSet(3);

  // start MBNet Task
  MBNET_TASK_Init(0);

  // start terminal
  TERMINAL_Init(0);
}


